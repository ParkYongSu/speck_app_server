package paidagogos.speck.sign_up;

public class Terms {
    private String service;
    private String personalInformationCollection;
    private String receiveEventInformation;

    public String getService() {
        return service;
    }

    public void setService(String service) {
        this.service = service;
    }

    public String getPersonalInformationCollection() {
        return personalInformationCollection;
    }

    public void setPersonalInformationCollection(String personalInformationCollection) {
        this.personalInformationCollection = personalInformationCollection;
    }

    public String getReceiveEventInformation() {
        return receiveEventInformation;
    }

    public void setReceiveEventInformation(String receiveEventInformation) {
        this.receiveEventInformation = receiveEventInformation;
    }
}
